class CalculadoraSimples:
    
    def somar(self, num1, num2):
        return num1 + num2
    
    def subtrair(self, num1, num2):
        return num1 - num2
    
    def multiplicar(self, num1, num2):
        return num1 * num2
    
    def ehPar(self, numero):
        return numero % 2 == 0
    
    def potencia(self, base, expoente):
        return base ** expoente